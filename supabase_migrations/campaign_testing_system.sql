-- Drop function if exists (for re-running migration)
DROP FUNCTION IF EXISTS update_variant_metrics() CASCADE;

-- Update variant metrics when performance changes
CREATE OR REPLACE FUNCTION update_variant_metrics()
RETURNS TRIGGER AS $$
BEGIN
    -- Calculate rates
    NEW.delivery_rate = CASE 
        WHEN NEW.emails_sent > 0 
        THEN ROUND((NEW.emails_delivered::DECIMAL / NEW.emails_sent) * 100, 2)
        ELSE 0 
    END;
    
    NEW.open_rate = CASE 
        WHEN NEW.emails_delivered > 0 
        THEN ROUND((NEW.emails_opened::DECIMAL / NEW.emails_delivered) * 100, 2)
        ELSE 0 
    END;
    
    NEW.click_rate = CASE 
        WHEN NEW.emails_opened > 0 
        THEN ROUND((NEW.emails_clicked::DECIMAL / NEW.emails_opened) * 100, 2)
        ELSE 0 
    END;
    
    NEW.reply_rate = CASE 
        WHEN NEW.emails_sent > 0 
        THEN ROUND((NEW.emails_replied::DECIMAL / NEW.emails_sent) * 100, 2)
        ELSE 0 
    END;
    
    NEW.meeting_rate = CASE 
        WHEN NEW.emails_replied > 0 
        THEN ROUND((NEW.meetings_booked::DECIMAL / NEW.emails_replied) * 100, 2)
        ELSE 0 
    END;
    
    NEW.actual_performance = NEW.reply_rate;
    NEW.updated_at = now();
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Drop trigger if exists (for re-running migration)
DROP TRIGGER IF EXISTS update_variant_metrics_trigger ON public.campaign_variants;

CREATE TRIGGER update_variant_metrics_trigger
    BEFORE UPDATE ON public.campaign_variants
    FOR EACH ROW
    EXECUTE FUNCTION update_variant_metrics();

