-- ============================================================================
-- ICP ANGLES SYSTEM
-- Messaging angles per ICP for A/B testing and varied outreach approaches
-- ============================================================================

-- Create the icp_angles table
CREATE TABLE IF NOT EXISTS public.icp_angles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    icp_profile_id BIGINT NOT NULL REFERENCES public.icp_profiles(id) ON DELETE CASCADE,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    
    -- Core angle definition
    name TEXT NOT NULL,                         -- "Cost Savings Angle", "Speed Angle"
    description TEXT,                           -- What this angle focuses on
    value_proposition TEXT NOT NULL,            -- Core message/pitch
    
    -- Messaging elements
    pain_points TEXT[] DEFAULT '{}',            -- Pain points this angle addresses
    hooks TEXT[] DEFAULT '{}',                  -- Opening hooks for emails
    proof_points TEXT[] DEFAULT '{}',           -- Social proof/stats to use
    objection_handlers TEXT[] DEFAULT '{}',     -- Common objections and responses
    
    -- Tone and style
    tone TEXT DEFAULT 'professional' CHECK (tone IN ('professional', 'casual', 'urgent', 'consultative', 'challenger')),
    
    -- Status
    is_active BOOLEAN DEFAULT true,
    is_control BOOLEAN DEFAULT false,           -- Mark as control for A/B tests
    
    -- Performance tracking
    performance_stats JSONB DEFAULT '{
        "emails_sent": 0,
        "opens": 0,
        "replies": 0,
        "positive_replies": 0,
        "meetings_booked": 0,
        "open_rate": 0,
        "reply_rate": 0,
        "positive_rate": 0
    }'::jsonb,
    
    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Add angle tracking to campaigns
DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns 
                   WHERE table_name = 'outreach_campaigns' AND column_name = 'angle_id') THEN
        ALTER TABLE public.outreach_campaigns ADD COLUMN angle_id BIGINT REFERENCES public.icp_angles(id) ON DELETE SET NULL;
    END IF;
    
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns 
                   WHERE table_name = 'outreach_campaigns' AND column_name = 'angle_ids') THEN
        ALTER TABLE public.outreach_campaigns ADD COLUMN angle_ids BIGINT[] DEFAULT '{}';
    END IF;
    
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns 
                   WHERE table_name = 'outreach_campaigns' AND column_name = 'ab_test_enabled') THEN
        ALTER TABLE public.outreach_campaigns ADD COLUMN ab_test_enabled BOOLEAN DEFAULT false;
    END IF;
END $$;

-- Add angle tracking to outreach queue for per-email tracking
DO $$ 
BEGIN
