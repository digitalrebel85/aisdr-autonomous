-- Function to update angle performance stats
CREATE OR REPLACE FUNCTION update_angle_performance(
    p_angle_id BIGINT,
    p_event_type TEXT  -- 'sent', 'opened', 'replied', 'positive_reply', 'meeting_booked'
)
RETURNS void AS $$
DECLARE
    v_stats JSONB;
    v_emails_sent INT;
    v_opens INT;
    v_replies INT;
    v_positive_replies INT;
    v_meetings INT;
BEGIN
    -- Get current stats
    SELECT performance_stats INTO v_stats
    FROM public.icp_angles
    WHERE id = p_angle_id;
    
    IF v_stats IS NULL THEN
        RETURN;
    END IF;
    
    -- Update the appropriate counter
    CASE p_event_type
        WHEN 'sent' THEN
            v_stats = jsonb_set(v_stats, '{emails_sent}', to_jsonb((v_stats->>'emails_sent')::int + 1));
        WHEN 'opened' THEN
            v_stats = jsonb_set(v_stats, '{opens}', to_jsonb((v_stats->>'opens')::int + 1));
        WHEN 'replied' THEN
            v_stats = jsonb_set(v_stats, '{replies}', to_jsonb((v_stats->>'replies')::int + 1));
        WHEN 'positive_reply' THEN
            v_stats = jsonb_set(v_stats, '{positive_replies}', to_jsonb((v_stats->>'positive_replies')::int + 1));
        WHEN 'meeting_booked' THEN
            v_stats = jsonb_set(v_stats, '{meetings_booked}', to_jsonb((v_stats->>'meetings_booked')::int + 1));
    END CASE;
    
    -- Recalculate rates
    v_emails_sent = (v_stats->>'emails_sent')::int;
    v_opens = (v_stats->>'opens')::int;
    v_replies = (v_stats->>'replies')::int;
    v_positive_replies = (v_stats->>'positive_replies')::int;
    
    IF v_emails_sent > 0 THEN
        v_stats = jsonb_set(v_stats, '{open_rate}', to_jsonb(ROUND((v_opens::numeric / v_emails_sent) * 100, 2)));
        v_stats = jsonb_set(v_stats, '{reply_rate}', to_jsonb(ROUND((v_replies::numeric / v_emails_sent) * 100, 2)));
    END IF;
    
    IF v_replies > 0 THEN
        v_stats = jsonb_set(v_stats, '{positive_rate}', to_jsonb(ROUND((v_positive_replies::numeric / v_replies) * 100, 2)));
    END IF;
    
    -- Update the record
    UPDATE public.icp_angles
    SET performance_stats = v_stats, updated_at = now()
    WHERE id = p_angle_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to get angle count for an ICP (for plan limit enforcement)
CREATE OR REPLACE FUNCTION get_icp_angle_count(p_icp_profile_id BIGINT)
RETURNS INT AS $$
BEGIN
    RETURN (
        SELECT COUNT(*)::int
        FROM public.icp_angles
        WHERE icp_profile_id = p_icp_profile_id
        AND is_active = true
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger to update updated_at
CREATE OR REPLACE FUNCTION update_icp_angles_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_update_icp_angles_updated_at ON public.icp_angles;
CREATE TRIGGER trigger_update_icp_angles_updated_at
    BEFORE UPDATE ON public.icp_angles
    FOR EACH ROW
    EXECUTE FUNCTION update_icp_angles_updated_at();

